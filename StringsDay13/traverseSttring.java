public class traverseSttring {
    public static void main(String[] args) {
        String str = "salmon bhai";
        // we use .lenght() function for finding the length of the string
        for (int i = 0; i < str.length(); i++) {
            // we use charAt() for finding element at position i
            System.out.println(str.charAt(i));
        }
    }

}
